package ar.uba.fi.talker;

public class Settings {

	private String pencilColor;
	private String pencilSize;

	public String getPencilColor() {
		return pencilColor;
	}

	public void setPencilColor(String pencilColor) {
		this.pencilColor = pencilColor;
	}

	public String getPencilSize() {
		return pencilSize;
	}

	public void setPencilSize(String pencilSize) {
		this.pencilSize = pencilSize;
	}

}
